    private void btnPrijaviSeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrijaviSeActionPerformed
        String username = txtUsername.getText().trim();
        String password = String.valueOf(passwordPolje.getPassword());
        
        try {
            validacija(username, password);
            
            Recepcioner r = new Recepcioner();
            r.setKorisnickoIme(username);
            r.setSifra(password);
            Recepcioner logovaniRecepcioner = Kontroler.getInstanca().pronadjiRecepcioneraLogin(r);
            if (logovaniRecepcioner == null) {
                JOptionPane.showMessageDialog(this, "Sistem ne moze da pronadje recepcionera na osnovu unetih vrednosti");
                txtUsername.setBorder(BorderFactory.createLineBorder(Color.RED));
                passwordPolje.setBorder(BorderFactory.createLineBorder(Color.RED));
            } else {
                Kontroler.getInstanca().uloguj(logovaniRecepcioner, this);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greska" + e.getMessage());
        }

    }//GEN-LAST:event_btnPrijaviSeActionPerformed

