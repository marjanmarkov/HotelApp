    private void validacija(String username, String password) throws Exception {
        Border border = BorderFactory.createLineBorder(Color.red);
        
        if ((username == null || username.isEmpty()) && (password == null || password.isEmpty())) {
            txtUsername.setBorder(border);
            passwordPolje.setBorder(border);
            throw new Exception("Sva polja su obavezna!");
        } else {
            txtUsername.setBorder(BorderFactory.createEtchedBorder());
            passwordPolje.setBorder(BorderFactory.createEtchedBorder());
        }
        
        if (username == null || username.isEmpty()) {
            txtUsername.setBorder(border);
            throw new Exception("Morate uneti username!");
        } else {
            txtUsername.setBorder(BorderFactory.createEtchedBorder());
        }
        
        if (password == null || password.isEmpty()) {
            passwordPolje.setBorder(border);
            throw new Exception("Morate uneti password!");
        } else {
            passwordPolje.setBorder(BorderFactory.createEtchedBorder());
        }
        
    }
    
